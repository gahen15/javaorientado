package aula01;

public class Code01 {

	public static void main(String[] args) {
		System.out.println("\tTipos Primitivos");

		byte numberByte = 5;
		short numberShort = 100;
		int numberInt = 1000;
		long numberLong = 1000000000000000000L;
		float numberFloat = 5.476578f;
		double numberDouble = 5.243652748328463;
		char character = 'E';
		boolean tipoBool = 10 < 5;

		System.out.println("Byte: "+numberByte + "\nShort: " + numberShort + "\nInt: " + numberInt + "\nLong: " + numberLong + "\nFloat: " + numberFloat
				+ "\nDouble: " + numberDouble + "\nChar: " + character + "\nBoolean: " + tipoBool);

	}

}
